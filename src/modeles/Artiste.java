package modeles;

public class Artiste {
	
	private int id_artiste;
	private String nom;
	
	public int getId_artiste() {
		return id_artiste;
	}
	public void setId_artiste(int id_artiste) {
		this.id_artiste = id_artiste;
	}
	public String getNom() {
		return nom;
	}
	public void setNom(String nom) {
		this.nom = nom;
	}
	


}